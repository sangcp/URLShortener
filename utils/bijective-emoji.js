const radix = base.length;

exports.encode = num => {
	let encoded = '';
	if (num === 0) return base[0];
	while (num > 0) {
		const index = num % radix;
		encoded = base[index] + encoded;
		num = Math.trunc(num / radix);
	}
	return encoded;
};
